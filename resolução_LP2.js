/*
Crie um algoritmo que calcule a velocidade média de um veículo e retorne com resultado. 
Fórmula: Vm(Velocidade média) = distância/tempo
*/

let distancia = 120;
let tempo = 2;
const vm = distancia / tempo;

console.log (`Gabriel decidiu pegar seu carro num sábado e dar uma volta pelo bairro onde havia acabado de se mudar.Ele percorreu 120.000 metros em 120 minutos, logo sua velocidade média em Km era de ${vm}Km.`)